package chapter2;

import java.util.Scanner;

public class SquareOfNumbers {
    public int squareOfNumberOne(int num1) {
        return num1 * num1;
    }

    public int squareOfNumberTwo(int num1) {
        return num1 * num1;
    }

    public int addSquares(int num1, int num2) {
        return num1 + num2;
    }

    public int subtractSquares(int num1, int num2) {
        return num1 - num2;
    }
}






















